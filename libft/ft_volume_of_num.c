#include "libft.h"

int		ft_volume_of_num(int x)
{
	int i;

	i = 0;
	if (x == -2147483648)
		return (10);
	if (x == 0)
		return (1);
	if (x < 0)
	{
		x = -x;
		i++;
	}
	while (x)
	{
		x = x / 10;
		i++;
	}
	return (i);
}
